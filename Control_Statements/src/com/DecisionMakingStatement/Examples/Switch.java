package com.DecisionMakingStatement.Examples;

public class Switch {
	public static void main(String[] args)
	{
		int num=10;
		switch(num)
		{
			case 10:
				System.out.println("Number is 10");
				break;
			case 20:
				System.out.println("Number is 20");
				break;
			case 30:
				System.out.println("Number is 30");
				break;
			default:
				System.out.println(num);
		}
	}
}
